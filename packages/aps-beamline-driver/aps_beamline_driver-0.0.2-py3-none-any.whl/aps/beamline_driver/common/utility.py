#import cv2 as cv
import numpy as np
from datetime import datetime

def datetime_string():
    return datetime.strftime(datetime.now(), "%Y-%m-%d_%H-%M-%S")

def generate_noise(histogram, noise, percentage_fluctuation):
    fluctuation = percentage_fluctuation*noise

    return histogram + noise + 0.5 * fluctuation - fluctuation * np.random.random(histogram.shape)

def calculate_projections_over_noise(histogram, noise_threshold, noise_mask=[0, 10, 0, 10], bad_pixels=[]):
    noise_level = np.average(histogram[noise_mask[0]:noise_mask[1], noise_mask[2]:noise_mask[3]])

    for x, y in bad_pixels: histogram[x, y] = noise_level

    histogram_on = np.copy(histogram)
    histogram_on[np.where(histogram < noise_threshold * noise_level)] = 0
    histogram_on[np.where(histogram >= noise_threshold * noise_level)] -= noise_level

    histogram_h = histogram_on.sum(axis=1)
    histogram_v = histogram_on.sum(axis=0)

    return histogram_on, histogram_h, histogram_v
