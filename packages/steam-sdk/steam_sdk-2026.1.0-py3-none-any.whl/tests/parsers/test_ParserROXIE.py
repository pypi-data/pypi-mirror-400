def area_of_triangle(x1, y1, x2, y2, x3, y3):
    return abs((x1 * (y2 - y3) + x2 * (y3 - y1) + x3 * (y1 - y2)) / 2)


def area_of_rectangle(x1, y1, x2, y2, x3, y3):
    side1 = ((x2 - x1) ** 2 + (y2 - y1) ** 2) ** 0.5
    side2 = ((x3 - x2) ** 2 + (y3 - y2) ** 2) ** 0.5
    return side1 * side2
