def license_text(license_name: str, author: str) -> str:
    if license_name == "MIT":
        return MIT_LICENSE.format(year=datetime.now().year, author=author or "Unknown")
    raise ValueError(f"Unsupported license: {license_name}")


def gh_actions_ci_python() -> str:
    return """\
name: CI

on:
  push:
  pull_request:

jobs:
  test:
    runs-on: ubuntu-latest
    steps:
      - uses: actions/checkout@v4
      - uses: actions/setup-python@v5
        with:
          python-version: "3.11"
      - run: python -m pip install -U pip
      - run: pip install -e ".[dev]"
      - run: pytest -q
"""


